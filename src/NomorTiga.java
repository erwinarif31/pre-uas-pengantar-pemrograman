public class NomorTiga {
    public static void main(String[] args) {
        String[] nama = {"Baco", "Becce", "Buccu"};
        int[] nim = {11109222, 11109223, 11109225};

        for (int i = 0; i < nama.length; i++) {
            System.out.printf("\nNama:  %s\n", nama[i]);
            System.out.printf("\nNIM:  %d\n", nim[i]);
            System.out.printf("\n-------------------------------\n");
        }
    }
}
